# Introduction to Linux Shell and Shell Scripting
1) Terminologies in Linux
    a) Kernel
    b) Shell
    c) Terminal
2) What is __Kernel__ ?
    Kernel is a core part /Heart in computers operating system
    which has complete control over everything in the operating system
    a) File Management
    b) Process Management
    c) I/O Management
    d) Memory Management
    e) Network Management
    f) Device Management
3) What is shell?
    A user program which provides interface to use operating system services 
    Takes input from user in human readable format and converts them into computer understanding format of through the kernal
4) *Shell* Is divided into two types:
    1) Command line shell
    2) Graphical Shell
